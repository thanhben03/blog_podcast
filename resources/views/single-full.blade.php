                    <p>1. Flaxseed Oil, Pumpkin Seed Oil and Hemp Seed Oil (tied): “These contain fairly high doses of omega-3 fatty acids from plant-sources, which are extremely healthy for us,” Hunnes explains, since omega-3 fatty acids decrease inflammation and control blood pressure. “They also contain good doses of monounsaturated fats, which likely reduce cholesterol.”
                        There’s a catch, though: Flaxseed oil, pumpkin seed oil and hemp seed oil all have relatively low smoke points — the temperatures at which an oil starts to burn and smoke — meaning they fare better in dressings, spreads and marinades than on the stovetop or in the oven.</p>
