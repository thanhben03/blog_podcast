                <div class="single-excerpt">
                    <p class="font-large">Most of us regard cooking oil as nothing more than a means to a non-sticking end. But (and this is a big, prepare-to-gag kind of but) the average American consumes a whopping 36 pounds of cooking oils per year — more than three times as much as in the early 1970s. These oils contributed more than 400 calories to our daily diet in 2010 (the Census Bureau suspiciously quit collecting data on how much fat and oil companies produce in 2011, meaning the Department of Agriculture can no longer use that data to accurately calculate how many calories cooking oil contributes to the average American diet).</p>
                </div>
                <div class="entry-main-content">
                    <h2>Let’s rank some oils</h2>
                    <hr class="wp-block-separator is-style-wide">
                    <p>All of this cooking oil isn’t exactly doing us any good, either: Physician and biochemist Cate Shanahan, author of Deep Nutrition: Why Your Genes Need Traditional Food, estimates that, at this point in time, roughly 45 percent of the average American’s calories come from refined oils. She’s also told me time and time again that consuming too much vegetable oil (an umbrella term for plant-based oils) can result in fatty liver disease, insulin resistance and migraines.</p>
                    <p>The lesson here: Cooking oils play a massive role in our overall health, which means choosing healthy oils is a bright idea if you expect to continue living for as long as humanly possible. To help us all make better choices, I asked Dana Hunnes, senior dietitian at the Ronald Reagan UCLA Medical Center and my go-to source for all nutritional queries, to help me rank every popular cooking oil by how healthy they are.</p>
                    <h2>With that as our guide</h2>
